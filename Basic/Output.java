public class Output {
    public static void main(String[] args) {

        // Text Printing
        System.out.println("Hey, my name is Moniaditya Singh."); // When we are working with text, it must be wrapped inside double quotations marks ("---");
        System.out.println("I am from Hajipur,Vaishali (Bihar)");

        // Number Printing
        System.out.println("My height is " + 5 + " feet " + 10 + " inch.");

        // There is also a print() method, which is similar to println().
        // The only difference is that it does not insert a new line at the end of the output.
        System.out.print("Aditya ");
        System.out.print("Singh ");
        System.out.print("Rajput");

    }
}
