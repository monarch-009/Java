public class Comments {

    public static void main(String[] args){

        // Single-line Comments
        // Single-line comments start with two forward slashes (//).
        // Most of the we put comments to write what my code in doing to make easy to understand for others

        //Example:
        System.out.println("Hello Java"); // Output of this code is Hello Java

        // Multi-line Comments
        // Multi-line comments start with /* and ends with */.
        // Any text between /* and */ will be ignored by Java.

        //Example
        /* The code below will print the words Hello Java Developer 
        to the screen, and it is amazing */
        System.out.println("Hello Java Developer");

    }
}