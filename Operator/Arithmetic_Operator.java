public class Arithmetic_Operator {
    public static void main(String[] args) {

        // Arithmetic operators
        int a = 10;
        int b = 100;

        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));

    }
}
